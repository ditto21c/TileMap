namespace TileMap.Data
{
    public enum BiomeType : byte
    {
        None = 0, // 바이옴 없음 / 미분류.
        Grassland = 1, // 초원 지역. 시작권, 일반 필드, 도로 주변 기본 지형.
        Ruins = 2, // 폐허 지역. 낡은 벽, 폐허 바닥, 상인/보물 지점이 섞이는 구역.
        Forest = 3, // 숲 지역. 나무, 덤불, 목재 자원, 자연 제단이 등장하는 구역.
        Swamp = 4, // 늪 지역. 얕은 늪, 깊은 늪, 물 지형이 등장하는 구역.
        Cave = 5, // 동굴/광산 지역. 동굴 바닥, 구덩이, 광산 레일, 은 광석이 등장하는 구역.
        Volcano = 6, // 화산 지역. 재 바닥, 용암, 화염 적, 미스릴 광석이 등장하는 구역.
        BossCore = 7, // 월드 중심 보스 코어. 보스 전장과 엔딩 포탈이 배치되는 구역.
    }
}
